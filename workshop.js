function drawSVG(svgpath) {
	beginShape();
	vertex(svgpath[0][0], svgpath[0][1]);
	for (let i = 1; i < svgpath.length; i++) {
		bezierVertex(svgpath[i][0], svgpath[i][1],
					 svgpath[i][2], svgpath[i][3],
					 svgpath[i][4], svgpath[i][5],);
	}
	endShape();
}

